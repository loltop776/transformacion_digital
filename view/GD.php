    <h2>Diagnóstico Interno</h2>
    <p>
        En la actualidad, la gestión documental dentro de la organización se realiza principalmente de manera manual, utilizando apoyos físicos como el papel. Este sistema, a pesar de ser tradicional, presenta numerosos inconvenientes que dificultan la eficiencia y la seguridad de la información.
        Uno de los principales problemas es la vulnerabilidad de los documentos físicos ante el paso del tiempo y posibles incidentes como deterioro, pérdidas accidentales, roturas o incluso incendios e inundaciones. Además, el almacenamiento físico supone una ocupación considerable de espacio, lo cual puede llegar a saturar archivadores, estanterías y otras zonas destinadas a la conservación de documentos. Esta acumulación provoca, además, dificultades en la localización rápida y efectiva de la información, incrementando los tiempos de espera y reduciendo la productividad.
        Otro aspecto preocupante es la carga de trabajo que implica llenar manualmente formularios, expedientes o solicitudes. Este proceso no solo es lento, sino que también es propenso a errores humanos, y puede dar lugar a documentos con datos poco claros, con caligrafía ilegible o incompletas, cosa que complica su posterior consulta y validación.
        Por todo esto, se considera necesario implementar una solución digital que modernice la gestión documental y permita garantizar la seguridad, accesibilidad y eficiencia de toda la información institucional.
    </p>
    <img class="normal_image_X" src="../img/gestor-documental.jpg" alt="Información pasada a digital">
    <h2>Soluciones en forma digital</h2>
    <p>
        Como hemos dicho en el apartado anterior en la actualidad, la gestión administrativa de documentos y datos dentro de la organización depende en gran medida de procesos manuales y del uso de documentos físicos. Este enfoque tradicional genera numerosos inconvenientes, entre los que destacan el uso excesivo de papel, la lentitud en los trámites y el riesgo constante de pérdida o deterioro de la información.
        Con el paso del tiempo, los documentos impresos pueden volverse difíciles de leer debido a manchas, roturas o una caligrafía poco clara, lo que dificulta su consulta y puede dar lugar a errores en la gestión. Además, la dependencia del soporte físico incrementa la posibilidad de perder información importante por la ausencia de copias de seguridad o por accidentes imprevistos.
        Las propuestas de soluciones en forma digital son para superar estas limitaciones, se propone una modernización integral del sistema mediante la implementación de un servicio de almacenamiento en la nube. Esta herramienta permitiría centralizar toda la información relacionada con el alumnado como expedientes académicos, plantillas de solicitudes y otros documentos institucionales de manera segura y accesible desde cualquier dispositivo con conexión a internet.
        También tendremos que establecer un sistema de copias de seguridad automatizadas a través de scripts que se ejecuten cada x tiempo. Estas copias se almacenarían en un dispositivo configurado con RAID 1, que duplicaría la información y la protegería ante posibles fallos de hardware. Esta infraestructura garantizaría la continuidad del servicio y la preservación de los datos de manera fiable.
    </p>

    <h2>Objectivos del PTD</h2>
    <p>
        Con la implementación de este plan de transformación digital se pretende alcanzar una serie de objetivos estratégicos que permitan optimizar los procesos internos de la organización:
        Reducir la dependencia del papel, fomentando una gestión documental más sostenible y eficiente.
        Minimizar los tiempos de respuesta y los procesos manuales que actualmente consumen muchos recursos humanos.
        Asegurar la integridad y disponibilidad de la información, mediante la digitalización y copias de seguridad sistemáticas.
        Facilitar el acceso remoto y seguro a los documentos desde cualquier lugar y dispositivo.
        Mejorar la transparencia, trazabilidad y control de todos los procesos documentales.
        Este conjunto de medidas contribuirá de forma decisiva a modernizar la gestión educativa y administrativa, al tiempo que fomenta una cultura digital entre los trabajadores y mejora el servicio ofrecido a la comunidad educativa.
    </p>
